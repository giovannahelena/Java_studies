public class Operations {
	public static void main(String[] args) {
		int	a;
		int	b;
		int	sum;
		int sub;
		int multi;
		float div;

		a = 3;
		b = 2;
		sum = a + b;
		sub = a - b;
		multi = a * b;
		div = (float) a / b;
		
		System.out.println(sum);
		System.out.println(sub);
		System.out.println(multi);
		System.out.println(div);
	}
}